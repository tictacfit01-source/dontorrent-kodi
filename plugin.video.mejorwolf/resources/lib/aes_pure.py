def _xtime(a):
    return (((a << 1) ^ 0x1b) & 0xff) if (a & 0x80) else (a << 1)


def _gmul(a, b):
    p = 0
    for _ in range(8):
        if b & 1:
            p ^= a
        a = _xtime(a)
        b >>= 1
    return p & 0xff


def _key_expansion(key):
    """AES key schedule. For AES-256, Nk=8, Nr=14, generates 60 32-bit words."""
    Nk = len(key) // 4
    assert Nk in (4, 6, 8)
    Nr = {4: 10, 6: 12, 8: 14}[Nk]
    Nb = 4
    w = [0] * (Nb * (Nr + 1))
    for i in range(Nk):
        w[i] = (key[4 * i] << 24) | (key[4 * i + 1] << 16) | (key[4 * i + 2] << 8) | key[4 * i + 3]
    for i in range(Nk, Nb * (Nr + 1)):
        temp = w[i - 1]
        if i % Nk == 0:
            # RotWord + SubWord + Rcon
            t0 = (temp >> 16) & 0xff
            t1 = (temp >> 8) & 0xff
            t2 = temp & 0xff
            t3 = (temp >> 24) & 0xff
            temp = (_SBOX[t0] << 24) | (_SBOX[t1] << 16) | (_SBOX[t2] << 8) | _SBOX[t3]
            temp ^= _RCON[i // Nk] << 24
        elif Nk > 6 and i % Nk == 4:
            t0 = (temp >> 24) & 0xff
            t1 = (temp >> 16) & 0xff
            t2 = (temp >> 8) & 0xff
            t3 = temp & 0xff
            temp = (_SBOX[t0] << 24) | (_SBOX[t1] << 16) | (_SBOX[t2] << 8) | _SBOX[t3]
        w[i] = (w[i - Nk] ^ temp) & 0xffffffff
    return w, Nr


def _inv_shift_rows(s):
    # s is a 16-byte list representing column-major state.
    return [
        s[0],  s[13], s[10], s[7],
        s[4],  s[1],  s[14], s[11],
        s[8],  s[5],  s[2],  s[15],
        s[12], s[9],  s[6],  s[3],
    ]


def _inv_sub_bytes(s):
    return [_SBOX_INV[b] for b in s]


def _add_round_key(s, w, rnd):
    out = []
    for c in range(4):
        word = w[rnd * 4 + c]
        out.append(s[4 * c]     ^ ((word >> 24) & 0xff))
        out.append(s[4 * c + 1] ^ ((word >> 16) & 0xff))
        out.append(s[4 * c + 2] ^ ((word >>  8) & 0xff))
        out.append(s[4 * c + 3] ^ (word & 0xff))
    return out


def _inv_mix_columns(s):
    out = list(s)
    for c in range(4):
        a0, a1, a2, a3 = s[4 * c], s[4 * c + 1], s[4 * c + 2], s[4 * c + 3]
        out[4 * c]     = _gmul(a0, 0x0e) ^ _gmul(a1, 0x0b) ^ _gmul(a2, 0x0d) ^ _gmul(a3, 0x09)
        out[4 * c + 1] = _gmul(a0, 0x09) ^ _gmul(a1, 0x0e) ^ _gmul(a2, 0x0b) ^ _gmul(a3, 0x0d)
        out[4 * c + 2] = _gmul(a0, 0x0d) ^ _gmul(a1, 0x09) ^ _gmul(a2, 0x0e) ^ _gmul(a3, 0x0b)
        out[4 * c + 3] = _gmul(a0, 0x0b) ^ _gmul(a1, 0x0d) ^ _gmul(a2, 0x09) ^ _gmul(a3, 0x0e)
    return out


def _aes_decrypt_block(block, w, Nr):
    s = list(block)
    s = _add_round_key(s, w, Nr)
    for rnd in range(Nr - 1, 0, -1):
        s = _inv_shift_rows(s)
        s = _inv_sub_bytes(s)
        s = _add_round_key(s, w, rnd)
        s = _inv_mix_columns(s)
    s = _inv_shift_rows(s)
    s = _inv_sub_bytes(s)
    s = _add_round_key(s, w, 0)
    return bytes(s)


def aes_cbc_decrypt(key, iv, ct):
    """AES-CBC decrypt. Returns padded plaintext bytes (caller unpads)."""
    if len(key) not in (16, 24, 32):
        raise ValueError("key must be 16/24/32 bytes")
    if len(iv) != 16:
        raise ValueError("iv must be 16 bytes")
    if len(ct) % 16 != 0 or len(ct) == 0:
        raise ValueError("ciphertext length must be a positive multiple of 16")
    w, Nr = _key_expansion(key)
    prev = iv
    out = bytearray()
    for i in range(0, len(ct), 16):
        block = ct[i:i + 16]
        dec = _aes_decrypt_block(block, w, Nr)
        out.extend(bytes(a ^ b for a, b in zip(dec, prev)))
        prev = block
    return bytes(out)


def pkcs7_unpad(data):
    if not data:
        raise ValueError("empty plaintext")
    n = data[-1]
    if n < 1 or n > 16 or data[-n:] != bytes([n]) * n:
        raise ValueError("bad PKCS7 padding")
    return data[:-n]


def evp_bytestokey_md5(password, salt, key_len=32, iv_len=16):
    """OpenSSL EVP_BytesToKey with MD5. Matches CryptoJS / GibberishAES
    "Salted__" format when called with key_len=32, iv_len=16."""
    if isinstance(password, str):
        password = password.encode("utf-8")
    if isinstance(salt, str):
        salt = salt.encode("latin-1")
    out = b""
    prev = b""
    while len(out) < key_len + iv_len:
        prev = hashlib.md5(prev + password + salt).digest()
        out += prev
    return out[:key_len], out[key_len:key_len + iv_len]


def decrypt_openssl_salted(b64_ciphertext, password):
    """Decrypt CryptoJS / openssl `Salted__` format. Accepts either the
    raw base64 (starts with U2FsdGVkX1...) or a double-base64-wrapped
    variant (where the outer base64 decodes to the inner base64).
    """
    import base64
    if isinstance(b64_ciphertext, bytes):
        b64_ciphertext = b64_ciphertext.decode("ascii", "ignore")
    raw = base64.b64decode(b64_ciphertext)
    if not raw.startswith(b"Salted__"):
        # enlacito.com wraps the already-base64 payload in base64 again,
        # so decoding once gives ascii "U2FsdGVk..." which decodes again
        # to the real "Salted__..." blob.
        try:
            raw2 = base64.b64decode(raw)
            if raw2.startswith(b"Salted__"):
                raw = raw2
        except Exception:
            pass
    if not raw.startswith(b"Salted__"):
        raise ValueError("not an OpenSSL Salted__ blob")
    salt = raw[8:16]
    ct = raw[16:]
    key, iv = evp_bytestokey_md5(password, salt, 32, 16)
    return pkcs7_unpad(aes_cbc_decrypt(key, iv, ct))
